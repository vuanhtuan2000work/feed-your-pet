import { readdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const currentDir = path.dirname(fileURLToPath(import.meta.url))
const defaultRoot = path.resolve(currentDir, '..')
const catRunDir = 'public/assets/pet/cat_actions/run'
const outputFile = 'src/data/generatedCatVariants.ts'
const folderPattern = /^(\d+)-(.+)-(lengend|legend)$/

function toId(name) {
  return name
    .trim()
    .replace(/[-\s]+/g, '_')
    .replace(/_+/g, '_')
    .toLowerCase()
}

function toLabel(name) {
  return name.replace(/[_-]+/g, ' ').replace(/\b\w/g, (letter) => letter.toUpperCase())
}

function makeSource(variants) {
  const body = JSON.stringify(variants, null, 2)
    .replace(/"([^"]+)":/g, '$1:')
    .replaceAll('"', "'")

  return [
    '// This file is generated by scripts/generate-cat-variants.mjs.',
    '// Add folders under public/assets/pet/cat_actions/run and restart dev/build to update it.',
    `export const GENERATED_CAT_VARIANTS = ${body} as const`,
    '',
  ].join('\n')
}

export async function generateCatVariants({ root = defaultRoot } = {}) {
  const absoluteCatRunDir = path.join(root, catRunDir)
  const entries = await readdir(absoluteCatRunDir, { withFileTypes: true }).catch(() => [])
  const variants = entries
    .filter((entry) => entry.isDirectory())
    .map((entry) => {
      const match = entry.name.match(folderPattern)
      if (!match) {
        return null
      }

      const [, orderText, rawName, suffix] = match
      const runFileStem = `${rawName}-${suffix}`
      return {
        order: Number(orderText),
        id: toId(rawName),
        label: toLabel(rawName),
        runFolder: entry.name,
        runFileStem,
      }
    })
    .filter(Boolean)
    .sort((first, second) => first.order - second.order)
    .map(({ order: _order, ...variant }) => variant)

  await writeFile(path.join(root, outputFile), makeSource(variants))
  return variants
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const variants = await generateCatVariants()
  console.log(`Generated ${variants.length} cat variants in ${outputFile}`)
}
